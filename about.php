            <h4>What is the BMF</h4>
           
         
            <p>BMF is a pioneering Business Management and Public Service Integrity professional development opportunity for the population of Cameroon in general, offered by Buea Municipal Council. It aims to overcome shortcomings in the development potential of ordinary citizens who constitute key players in the social, economic and political life of Cameroon and Buea municipality in particular. It is a partnership between the Buea Municipal Council and the International Governance Institute of the UK through its Cameroon chapter known as the Focal Integrity Team. The BMF supports young public servants in the civil service and private sectors, unskilled small business operators and young entrepreneurs; graduates from all works of life, including individuals who desire to improve their competencies in business management and local governance for the sustainable development of Buea Municipality and Cameroon in general.</p>
           
            <p>The purpose of BMF is to help Cameroon recover lost grounds in governance, economic and social development as quickly as possible so as to keep pace and secure the realization of the National Vision by 2035. To this end the BMF’s professional development trainings and modules have been designed to provide implementation support at local government levels, to the following government policies and frameworks for Growth, Employment, Sustainable Development and Accountable National Governance:</p>
            <p><ul>
            <li>The National Anti-Corruption Strategy of Cameroon </li>
            <li>International Monetary Fund (IMF)’s Enhanced Structural Adjustment Facility Medium-Term Economic and Financial Policy Framework </li>
            <li>Cameroon’s Development Strategy as expressed in the 2009 Growth and Employment Strategy and 2035 Vision put forward by the Ministry of Economy, Planning and Regional Development ( also known as WB Country Assistance Strategy for Cameroon)</li>
            <li>The National Governance Program [Project] of June, 2000 supported by the African Development Bank (AfDB). </li>
            </ul>
            </p>
            <p><h4>Fellowship Facilitators</h4></p>
            <p>BMF lecturers and facilitators are leading business management, anti corruption and good governance experts and practitioners from African/European universities and organizations; supported technically by a group of former chief executives from International Development organizations and the World Bank at Partnership for Transparency Fund in Washington DC.</p>
            <p>The BMF will set the pace for the promotion of a sustainable private and public sector development driven by ordinary citizens to ensure improve governance, quality human resource for investment and development, thereby promoting collective well being as well as shared prosperity.</p>
            <p><h4>Fellowship Objectives  (BMF)</h4></p>
            <p>Bearing in mind that the message of refusing to give or take a bribe will not erase poor governance overnight including the shortfall of integrity and professionalism by business and public operators in the Cameroonian system, IGI-FITCAM is engaging in a unique innovation of changing mindsets and attitudes through an approach of collective education, skill development support and sharing of best practices in business administration and public service governance; to improve civic performance and curb public sector corruption, thereby reducing the corruption perception index of Cameroon internationally and nationally.</p>
            <p><h4>In this respect BMF has the following as objectives:</h4></p>
            <p>Promote professional education which is tailored to support the realization of our national development policies and growth strategies in public life through the stratagem of municipal council fellowships </p>

            <p><h4>Short and medium term:</p></h4></p>
            <p>To provide training in best practices in business administration and management to 10,000 private sector operators in the municipality over a period of 5 years; and to be continued after the pilot phase for a lifelong period.</p>
            <p>To increase the ability of 20,000 private and public sector operators in local metropolis to contribute to efforts of transparency, integrity and accountability in their distinct sectors of assignment thereby engaging them as agents of change</p>
            <p>To support the Governance, Growth and Development Agenda of the Government of Cameroon as established through policy by means of quality human resource development and learning against corruption</p>
            <p>To increase Cameroon Human Resource Competitiveness locally thereby ensuring investors security in the Municipality including improvement in well-being of the population.</p>

           


